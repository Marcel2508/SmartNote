  <h3 style="text-align:center;">Tutorial:</h3>
  On the left Side you can add Notes or Folders by clicking the Add Button.<br/>
  This will add an Entry to the file tree.<br/>
  The newly created file will be named "undefined" or "New Folder".<br/>
  By double clicking a file or a folder a popup appears, where you can change it's name or delete the selected File/Folder.
  <br/><br/>
  When opening a note, you can view it's rendered Preview on the right hand side.<br/>
  All common Markdown expressions as well as Code syntax hilighting are supported.<br/><br/>
